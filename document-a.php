	</p>
	<p>
		<b>Introduction</b><br>
		In previous IRG discussions, there is considerable controversy of the addition of new UCV rules which unify current forms of components with historial forms where the same historical form may be shared by different components in different historical periods.
	</p>
	<p>
		The first recorded case of this issue is in IRG #51, where the UCV rule unifying <img src="https://cdn.jsdelivr.net/gh/yi-bai/iwds@master/glyphs/u96e8-03.png" class=head-character> and <img src="https://cdn.jsdelivr.net/gh/yi-bai/iwds@master/glyphs/u96e8-03-itaiji-012.png" class=head-character> was controversial because <img src="https://cdn.jsdelivr.net/gh/yi-bai/iwds@master/glyphs/u96e8-03-itaiji-012.png" class=head-character> is also a common variant form of <img src="https://glyphwiki.org/glyph/u864d-05.svg" class=head-character>. <img src="https://cdn.jsdelivr.net/gh/yi-bai/iwds@master/glyphs/u96e8-03.png" class=head-character> and <img src="https://cdn.jsdelivr.net/gh/yi-bai/iwds@master/glyphs/u96e8-03-itaiji-012.png" class=head-character> were added as UCV rule #188a with the description "Only for rain".
	</p>
	<p>
		The second recorded case of this issue is in IRG #56, where the UCV rule unifying <img src="https://cdn.jsdelivr.net/gh/yi-bai/iwds@master/glyphs/u74e6.png" class=head-character> and <img src="https://cdn.jsdelivr.net/gh/yi-bai/iwds@master/glyphs/u2d0ac.png" class=head-character> was controversial because <img src="https://cdn.jsdelivr.net/gh/yi-bai/iwds@master/glyphs/u2d0ac.png" class=head-character> form as a component is not always etymologically related to <img src="https://cdn.jsdelivr.net/gh/yi-bai/iwds@master/glyphs/u74e6.png" class=head-character>. <img src="https://cdn.jsdelivr.net/gh/yi-bai/iwds@master/glyphs/u74e6.png" class=head-character> and <img src="https://cdn.jsdelivr.net/gh/yi-bai/iwds@master/glyphs/u2d0ac.png" class=head-character> were added as UCV rule #439 with the description "Only for etymological relation with 瓦(tile)".
	</p>
	<p>
		There is some resistance to the addition of these rules because of the concern of over-application of UCV rules leading to increased misunified or over-unified characters.  Also there is a concern that the proof of burden was not clear.</p>
	<p>
		<b>Proposal</b>
	</p>
	<p>
		This document proposes that:<br>
	</p>
	<ol>
		<li>The meaning of non-cognate rule should be updated to reflect actual usage by IRG. The PnP should clarify the long standing convention that characters related in historical derivation are also applicable for the non-cognate rule as long as the semantics are sufficiently different. It is suggested to add the following text in <i>2.1.3. Non-cognate Rule</i>:</li>
		<blockquote>
			Characters that are related in historical derivation may also be disunified as long the difference in glyph shape is sufficient for reflecting different semantics.  An example is 間 and 閒 which are related in historial derivation but are disunified because they have different semantics and typically used in mutually exclusive contexts in present day use. For the purpose of IRG processes, these characters have been and are still considered applicable for the non-cognate rule even if they are related in historical derivation and technically cognate.
		</blockquote>
		<li>The prior precedent for constraining the application of a particular UCV rule to an etymological relationship be written as a standard procedure into the PnP. It is suggested to add the following text under section <i>2.5.2 Discussion Procedure: (1) For unification issues</i> to clarify the circumstances of enforcing this requirement:</li>
		<blockquote>
			To avoid misunification of ideographs, IRG may specify etymological constraints to the application of a particular UCV rule, i.e. an etymological relationship must be proven between a proposed character and a coded or another Working Set character for the rule to be applicable.
		</blockquote>
		<li>The circumstances for adding an etymological relationship for a UCV rule be specified. It is suggested to add the following text subsequent to the previous paragraph:</li>
		<blockquote>
			An etymological constraint will only be specified for a particular UCV rule when one or more of the suggested unifiable forms may typically be etymologically related to another radical or component, thus the rule is at high risk of causing misunification of unrelated characters.
		</blockquote>
		<li>The proof of burden be specified.  It is suggested to add following text subsequent to the previous paragraph:</li>
		<blockquote>
			The unification shall only apply when there is sufficient evidence to prove that the two characters in discussion are etymologically related, i.e. the proof of burden lies in the reviewer instead of the submitter.  A proposed character will also not be postponed unless there is reasonable doubt that the character is etymologically related with another coded or Working Set character.
		</blockquote>
		<li>Re-iterate that characters with different semantics are still applicable for disunification under the non-cognate rule even if it satisfies the etymological requirement.  It is suggested to add following text subsequent to the previous paragraph:</li>
		<blockquote>
			Even if an etymological relationship can be proved between two characters, the non-cognate rule still applies, i.e. as long as there is sufficient evidence to show that the two characters are used with mutually exclusive semantics in a certain region or language, the characters will not be unified.
		</blockquote>
	</ol>
